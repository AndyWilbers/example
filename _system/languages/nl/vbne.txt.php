			function TXT_VBNE_PAGE_HEADER($request = array()){
				
			//	Check request:	
				if (!is_array($request)) { return '';}
				if (count($request) == 0){ return '';}
				
			//	Return page_header:
				switch ( strtolower(trim($request[0])) ){
					case 'sleutels':
					return 'sleutels';
					break;
					
					case 'account':
					case 'my_reports':
					case 'my_observations':
					return 'account';
					break;
					
					case 'pdf':
					return 'pdf';
					break;
					
					default:
					return  'info';
					break;
				}
				
			}
			
			function TXT_VBNE_CRUMB ($name){
			
			// 	Names for path to crumb:
				$names = array();
				$names['my reports']  		= TXT_VBNE_LBL_MY_REPORTS;
				$names['my observations']  	= TXT_VBNE_LBL_MY_OBSERVATIONS;
				$names['observaties']  		= TXT_VBNE_LBL_OBSERVATIONS;
				$names['calculaties']  		= TXT_VBNE_LBL_CALCULATIONS;
				
				$name = strtolower(trim($name));
				
				return array_key_exists($name, $names)? strtolower(trim($names[$name])) : $name;
			}